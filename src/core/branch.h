#ifndef CORE_BRANCH_h
#define CORE_BRANCH_h

////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

enum class BranchType {
    INVALID,
    DIRECT,
    INDIRECT,
    DIRECT_CALL,
    INDIRECT_CALL,
    CONDITIONAL,
    RETURN
};

////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

inline bool br_is_direct(BranchType t)
{
    return t == BranchType::DIRECT || t == BranchType::DIRECT_CALL;
}

inline bool br_is_indirect(BranchType t)
{
    return t == BranchType::INDIRECT || t == BranchType::INDIRECT_CALL;
}

////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

#endif  // CORE_BRANCH_h
