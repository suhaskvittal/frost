#ifndef TRANSACTION_h
#define TRANSACTION_h

////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

enum class TransactionType { READ, WRITE, PREFETCH, TRANSLATION };

inline bool trans_is_read(TransactionType& t)
{
    return t != TransactionType::WRITE;
}

////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

struct Transaction
{
    uint8_t         coreid;
    uint16_t        robid;
    TransactionType type;

    uint64_t address;
    bool     address_is_ip;

    Transaction(uint8_t cid, uint16_t rid, TransactionType t, uint64_t addr, bool addr_is_ip=false)
        :coreid(cid),
        robid(rid),
        type(t),
        address(addr),
        address_is_ip(addr_is_ip)
    {}
};

////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

#endif  // TRANSACTION_h
